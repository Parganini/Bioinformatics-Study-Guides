function makeLesson({
  id,
  code,
  module,
  date,
  title,
  summary,
  tags,
  products,
  lessonType,
  slides,
  transcript,
  extra = [],
  priority = "high",
  okTopics = [],
  skippedTopics = [],
  aliases = [],
}) {
  return {
    id,
    code,
    module,
    date,
    status: "available",
    title,
    summary,
    tags,
    products,
    lessonType,
    componentKey: id,
    aliases,
    driveFolder: module === "module-1" ? IBDPI_DRIVE.module1 : IBDPI_DRIVE.module2,
    resources: {
      slides,
      transcript,
      recording: todoRecording,
      extra,
    },
    slideImage: slideImageTodo,
    examScope: {
      priority,
      source: "Corso OK/SKIP + transcript",
      okTopics,
      skippedTopics,
    },
  };
}

